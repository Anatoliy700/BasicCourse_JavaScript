// Здесь решение через объявление переменной через ключевое слово let для тогго чтобы уйти от глобальной
// области видимости.
var arr = [];
for (let i = 0; i < 3; i++) {
  arr.push(function() { console.log(i) });
}

arr[0]();
arr[1]();
arr[2]();
