// Здесь решение через функцию обертку для тогго чтобы уйти от глобальной области видимости.
var arr = [];
for (var i = 0; i < 3; i++) {
  (function() {
    var k = i;
    arr.push(function() { console.log(k) });
  })()}

arr[0]();
arr[1]();
arr[2]();
